package models;

public class Akademisyen {

	String ad;
	String soyad;
	String email;
	String sifre;
	String resim;
	int bolum_id;
	int unvan_id;
	
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public String getSoyad() {
		return soyad;
	}
	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSifre() {
		return sifre;
	}
	public void setSifre(String sifre) {
		this.sifre = sifre;
	}
	public String getResim() {
		return resim;
	}
	public void setResim(String resim) {
		this.resim = resim;
	}
	public int getBolum_id() {
		return bolum_id;
	}
	public void setBolum_id(int bolum_id) {
		this.bolum_id = bolum_id;
	}
	public int getUnvan_id() {
		return unvan_id;
	}
	public void setUnvan_id(int unvan_id) {
		this.unvan_id = unvan_id;
	}
	
}
